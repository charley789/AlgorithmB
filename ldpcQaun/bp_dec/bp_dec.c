//-- BP init and gen syndrome: bias => A->tt => syndrome chk => return 1 if pass; otherwise init bp and return 0
uint8_t bp2_init22(BP2_Ctl *bp, double bias[][2], a2_matrix *A, uint8_t *target_zz)
{
  /* ------------------------------------------
   *  (1) 先準備偵錯檔：追加模式
   * ------------------------------------------*/
  

  /* ---- (2) 原有宣告 ------------------------ */
  int32_t m, n, j;
  double dn[N]; /* qn[0] - qn[1]               */
  double pn_afv[2];

  /* ------------------------------------------
   *  (3)  把 target_zz 複製並列印
   * ------------------------------------------*/
  memcpy(bp->target_z, target_zz, sizeof(bp->target_z));

  /* ------------ (4) 用先驗做第一次硬判定 ---------------- */
  for (n = 0; n < A->NN; n++)
    bp->tt[n] = (bias[n][0] > 0.5) ? 0 : 1;

  GenSyndrome2(A, bp->tt, bp->zz);

  if (HamDist(bp->zz, bp->target_z, A->MM) == 0)
  {
    return 1;
  }

  /* ------------ (5) 將 bias 複製到 pn ------------------- */
  memcpy(bp->pn, bias, A->NN * 2 * sizeof(**bias));

  /* ------------ (6) 逐節點處理 -------------------------- */
  for (n = 0; n < A->NN; n++)
  {

    restrict_p2(bp->pn[n]); /* range clipping */

    pn_afv[0] = bp->pn[n][0];
    pn_afv[1] = bp->pn[n][1];
    dn[n] = pn_afv[0] - pn_afv[1];

    for (j = 0; j < A->num_n[n]; j++)
      memcpy(bp->qlm[n][j], pn_afv, 2 * sizeof(**bias));
  }

  memcpy(bp->qn, bp->pn, A->NN * 2 * sizeof(**bias));

  /* ------------ (7) 以 dn 初始化 dqml  ------------------- */
  for (m = 0; m < A->MM; m++)
    for (j = 0; j < A->num_m[m]; j++)
    {
      n = A->mlist[m][j];
      bp->dqml[m][j] = dn[n];
    }
  return 0;
}

//-- BP dec (one call one iter): return 1 syndrome OK, 0 still NG
uint8_t bp2_dec20(BP2_Ctl *bp, a2_matrix *A)
{
  // int32_t  m, n, i , j ;

  int32_t m, j;
  //-- Horizontal Step of MacKay's '99 IT paper
  for (m = 0; m < A->MM; m++)
  { // update all dm[M]
    ////bp->dm[m] = 1.0;
    bp->dm[m] = (double)bp->target_z[m] * (-2) + 1; // 0 -> 1,  1 -> -1    // SYNDROME_DEC

    for (j = 0; j < A->num_m[m]; j++)
    {
      // n = A->mlist[m][j];
      bp->dm[m] *= bp->dqml[m][j];
    }
    // if(bp->dm[m] == 0) {  printf("hit bp->dm[%d] == 0 , need debug !! \n", m); getchar(); }

    for (j = 0; j < A->num_m[m]; j++)
    { // update all dml[m][L], L =num_m[m]
      // n = A->mlist[m][j];
      // if(bp->dqml[m][j] == 0) { printf(" !!! to be /bp->dqml[%d][%d]=0 , need debug !!! \n",m,j); getchar(); }
      bp->dml[m][j] = bp->dm[m] / bp->dqml[m][j];
      bp->rml[m][j][0] = (1.0 + bp->dml[m][j]) / 2; // and hence can update rml[0]
      bp->rml[m][j][1] = (1.0 - bp->dml[m][j]) / 2; // and also rml[1]
      // if(!(0<bp->rml[m][j][0]&&bp->rml[m][j][0]<=1)) { printf(" !!! rml[%d][%d][0]=%g out of range , debug !!! \n", m,j,bp->rml[m][j][0]); getchar(); }
      // if(!(0<bp->rml[m][j][1]&&bp->rml[m][j][1]<=1)) { printf(" !!! rml[%d][%d][1]=%g out of range , debug !!! \n", m,j,bp->rml[m][j][1]); getchar(); }
    }
  }

  int32_t n, i;
  //-- Vertical Step of MacKay's '99 IT paper
  for (n = 0; n < A->NN; n++)
  {
    //- mark this row to use qn
    bp->qn[n][0] = bp->pn[n][0];
    bp->qn[n][1] = bp->pn[n][1];

    for (i = 0; i < A->num_n[n]; i++)
    {
      m = A->nlist[n][i];
      j = A->ni2j[n][i];
#if ALFA
      bp->rml[m][j][0] = pow_fast(bp->rml[m][j][0], 100.0 / ALFA);
      bp->rml[m][j][1] = pow_fast(bp->rml[m][j][1], 100.0 / ALFA);
#endif
      // if(bp->rml[m][j][0] == 0) { printf(" !!! to be *bp->rml[%d][%d][0]=0 !!! \n", m,j); getchar(); }
      // if(bp->rml[m][j][1] == 0) { printf(" !!! to be *bp->rml[%d][%d][1]=0 !!! \n", m,j); getchar(); }
      // if(bp->rml[m][j][0]==0 && bp->rml[m][j][1]==0) { printf(" !!! both bp->rml[%d][%d][0] and [1] be zero at n = %d !!! \n", m,j,n); getchar(); }

#if AFP == 0
      bp->qn[n][0] *= bp->rml[m][j][0];
      bp->qn[n][1] *= bp->rml[m][j][1];
#else
      bp->qn[n][0] *= pow_fast(bp->rml[m][j][0], 100.0 / AFP);
      bp->qn[n][1] *= pow_fast(bp->rml[m][j][1], 100.0 / AFP);
#endif
      // if(!(0<=bp->qn[n][0]&&bp->qn[n][0]<=1)) { printf(" !!! bp->qn[%d][0]=%g out of range !!! \n", n,bp->qn[n][0]); getchar(); }
      // if(!(0<=bp->qn[n][1]&&bp->qn[n][1]<=1)) { printf(" !!! bp->qn[%d][1]=%g out of range !!! \n", n,bp->qn[n][1]); getchar(); }
    }
    //- corner values
    /*if(bp->qn[n][0] == 0 && bp->qn[n][1] == 0) { printf("both bp->qn[n][0] = bp->qn[n][0]=0 at n = %d , debug !! \n ", n); getchar(); }
    else if(bp->qn[n][0] == 0) { printf("both bp->qn[n][0] =0 at n = %d , debug !! \n ", n); getchar(); }
    else if(bp->qn[n][1] == 0) { printf("both bp->qn[n][1] =0 at n = %d , debug !! \n ", n); getchar(); }*/

    for (i = 0; i < A->num_n[n]; i++)
    {
      m = A->nlist[n][i];
      j = A->ni2j[n][i];
      // if(bp->rml[m][j][0] == 0) { printf(" !!! to be bp->qn[n][0]=%g/bp->rml[%d][%d][0]=0 at n = %d !!! \n", bp->qn[n][0],m,j,n); getchar(); }
      // if(bp->rml[m][j][1] == 0) { printf(" !!! to be bp->qn[n][0]=%g/bp->rml[%d][%d][1]=0 at n = %d !!! \n", bp->qn[n][1],m,j,n); getchar(); }
      bp->qlm[n][i][0] = bp->qn[n][0] / bp->rml[m][j][0];
      bp->qlm[n][i][1] = bp->qn[n][1] / bp->rml[m][j][1];

#if ALFA_V
      bp->qlm[n][i][0] = pow_fast(bp->qlm[n][i][0], 10.0 / ALFA_V);
      bp->qlm[n][i][1] = pow_fast(bp->qlm[n][i][1], 10.0 / ALFA_V);
#endif

      //- normalizing qlm
      bp->qlm[n][i][0] = bp->qlm[n][i][0] / (bp->qlm[n][i][0] + bp->qlm[n][i][1]); // bp->qlm[n][i][1] = 1.0 - bp->qlm[n][i][0];
      restrict_p2(bp->qlm[n][i]);
      //- update dqml
      bp->dqml[m][j] = bp->qlm[n][i][0] - bp->qlm[n][i][1];
      // if(fabs(bp->dqml[m][j]) < 2*dlt) { printf("get too small abs from bp->dqml[%d][%d] %g , need debug !!! \n", m,j,bp->dqml[m][j]); getchar(); }
    }
    //- normalizing qn (can skip now)
    ////bp->qn[n][0] = bp->qn[n][0]/(bp->qn[n][0]+bp->qn[n][1]);    //bp->qn[n][1] = 1.0 - bp->qn[n][0];
    ////restrict_p2(bp->qn[n])
  }

  //-- Syndrome Checking
  for (n = 0; n < A->NN; n++)
  {
    ////if(bp->qn[n][0] > 0.5)    bp->tt[n] = 0;
    ////else                      bp->tt[n] = 1;
    if (bp->qn[n][0] > bp->qn[n][1])
      bp->tt[n] = 0; // use this as we don't have qn normalized now
    else
      bp->tt[n] = 1; // use this as we don't have qn normalized now
  }
  GenSyndrome2(A, bp->tt, bp->zz);
  ////if( is_zero_vec(bp->zz, M) )  return 1;
  ////else                          return 0;
  if (0 == HamDist(bp->zz, bp->target_z, A->MM))
    return 1; // SYNDROME_DEC
  else
    return 0; // SYNDROME_DEC
}

//-- base on bp_dec4 but decode to target_z (support SYNDROME_DEC)
uint8_t bp2_dec24(BP2_Ctl *bp, a2_matrix *A)
{
  int32_t m, n, i, j;

  for (m = 0; m < A->MM; m++)
  { // make all dm[M] ready first!!
    // bp->dm[m] = 1.0;                                                            // NO_TARGET_Z
    bp->dm[m] = (double)bp->target_z[m] * (-2) + 1; // zm 0 -> 1,  zm 1 -> -1      // BY_TARGET_Z

    for (j = 0; j < A->num_m[m]; j++)
    {
      bp->dm[m] *= bp->dqml[m][j];
    }
  }

  for (n = 0; n < A->NN; n++)
  {
    bp->qn[n][0] = bp->pn[n][0];
    bp->qn[n][1] = bp->pn[n][1];

    for (i = 0; i < A->num_n[n]; i++)
    { // update n <- dml[m][j]
      m = A->nlist[n][i];
      j = A->ni2j[n][i];
      bp->dml[m][j] = bp->dm[m] / bp->dqml[m][j];
      bp->rml[m][j][0] = (1.0 + bp->dml[m][j]) / 2; // and hence can update rml[0]
      bp->rml[m][j][1] = (1.0 - bp->dml[m][j]) / 2; // and also rml[1]

#if AFP == 0
      bp->qn[n][0] *= bp->rml[m][j][0];
      bp->qn[n][1] *= bp->rml[m][j][1];
#else
      bp->qn[n][0] *= pow_fast(bp->rml[m][j][0], 100.0 / AFP);
      bp->qn[n][1] *= pow_fast(bp->rml[m][j][1], 100.0 / AFP);
#endif
    }

    for (i = 0; i < A->num_n[n]; i++)
    { // update qlm[n][i] -> m  (by delta and m-friendly index dqml[m][j] -> m)
      m = A->nlist[n][i];
      j = A->ni2j[n][i];
      bp->qlm[n][i][0] = bp->qn[n][0] / bp->rml[m][j][0];
      bp->qlm[n][i][1] = bp->qn[n][1] / bp->rml[m][j][1];

      //- normalizing qlm
      bp->qlm[n][i][0] = bp->qlm[n][i][0] / (bp->qlm[n][i][0] + bp->qlm[n][i][1]); // bp->qlm[n][i][1] = 1.0 - bp->qlm[n][i][0];
      restrict_p2(bp->qlm[n][i]);
      //- update dqml
      bp->dqml[m][j] = bp->qlm[n][i][0] - bp->qlm[n][i][1];

      //- make dm more updated
      bp->dm[m] = bp->dml[m][j] * bp->dqml[m][j];
    }
    //- normalizing qn (can skip now)
    ////bp->qn[n][0] = bp->qn[n][0]/(bp->qn[n][0]+bp->qn[n][1]);    //bp->qn[n][1] = 1.0 - bp->qn[n][0];
    ////restrict_p2(bp->qn[n])
  }

  //-- Syndrome Checking
  for (n = 0; n < A->NN; n++)
  {
    ////if(bp->qn[n][0] > 0.5)    bp->tt[n] = 0;
    ////else                      bp->tt[n] = 1;
    if (bp->qn[n][0] > bp->qn[n][1])
      bp->tt[n] = 0; // use this as we don't have qn normalized now
    else
      bp->tt[n] = 1; // use this as we don't have qn normalized now
  }
  GenSyndrome2(A, bp->tt, bp->zz);
  /*if( is_zero_vec(bp->zz, M) )  return 1;                   // NO_TARGET_Z
  else                          return 0;*/
  if (0 == HamDist(bp->zz, bp->target_z, A->MM))
    return 1; // BY_TARGET_Z
  else
    return 0;
}



