void load_G2(FILE *fpG, g_matrix_GFQ *G, const char *label)
{
    if (!fpG) {
        fprintf(stderr, "load_G2: FILE pointer is NULL for %s\n", label);
        exit(1);
    }

    int32_t err, val;
    int32_t m, n, i, j;

    // --- header: n m ---
    err = fscanf(fpG, "%d %d", &(G->NN), &(G->MM));
    if (err == EOF) { fprintf(stderr, "scan %s n m NG !!\n", label); exit(1); }

    // --- dc_max dr_max ---
    err = fscanf(fpG, "%d %d", &(G->max_num_n), &(G->max_num_m));
    if (err == EOF) { fprintf(stderr, "scan %s dc_max dr_max NG !!\n", label); exit(1); }

    printf("%s->[NN,MM,dc_max,dr_max] = [%d %d %d %d]\n",
           label, G->NN, G->MM, G->max_num_n, G->max_num_m);

    // --- column degrees (len = NN) ---
    for (n = 0; n < G->NN; n++) {
        err = fscanf(fpG, "%d", &(G->num_n[n]));
        if (err == EOF) { fprintf(stderr, "scan %s col deg NG at n=%d !!\n", label, n); exit(1); }
        if (G->num_n[n] < 0 || G->num_n[n] > G->max_num_n) {
            fprintf(stderr, "bad %s num_n[%d]=%d (dc_max=%d)\n",
                    label, n, G->num_n[n], G->max_num_n);
            exit(1);
        }
    }

    // --- row degrees (len = MM) ---
    for (m = 0; m < G->MM; m++) {
        err = fscanf(fpG, "%d", &(G->num_m[m]));
        if (err == EOF) { fprintf(stderr, "scan %s row deg NG at m=%d !!\n", label, m); exit(1); }
        if (G->num_m[m] < 0 || G->num_m[m] > G->max_num_m) {
            fprintf(stderr, "bad %s num_m[%d]=%d (dr_max=%d)\n",
                    label, m, G->num_m[m], G->max_num_m);
            exit(1);
        }
    }

    // --- nlist: per column, read dc_max indices (1-based, 0 is pad) ---
    for (n = 0; n < G->NN; n++) {
        G->nlist[n] = calloc(G->num_n[n], sizeof(*(G->nlist[n])));
        for (i = 0; i < G->max_num_n; i++) {
            err = fscanf(fpG, "%d", &val);
            if (err == EOF) { fprintf(stderr, "scan %s nlist[%d] NG !!\n", label, n); exit(1); }
            if (i < G->num_n[n]) {
                if (val <= 0 || val > G->MM) {
                    fprintf(stderr, "bad %s nlist[%d][%d]=%d (MM=%d)\n",
                            label, n, i, val, G->MM);
                    exit(1);
                }
                G->nlist[n][i] = val - 1;  // to 0-based
            }
            // else: padding, just consume
        }
    }

    // --- mlist: per row, read dr_max indices (1-based, 0 is pad) ---
    for (m = 0; m < G->MM; m++) {
        G->mlist[m] = calloc(G->num_m[m], sizeof(*(G->mlist[m])));
        for (j = 0; j < G->max_num_m; j++) {
            err = fscanf(fpG, "%d", &val);
            if (err == EOF) { fprintf(stderr, "scan %s mlist[%d] NG !!\n", label, m); exit(1); }
            if (j < G->num_m[m]) {
                if (val <= 0 || val > G->NN) {
                    fprintf(stderr, "bad %s mlist[%d][%d]=%d (NN=%d)\n",
                            label, m, j, val, G->NN);
                    exit(1);
                }
                G->mlist[m][j] = val - 1;  // to 0-based
            }
            // else: padding, just consume
        }
    }

    // --- optional: sum check ---
    long sum_col = 0, sum_row = 0;
    for (n = 0; n < G->NN; n++) sum_col += G->num_n[n];
    for (m = 0; m < G->MM; m++) sum_row += G->num_m[m];
    if (sum_col != sum_row) {
        fprintf(stderr, "[WARN] %s sum(col deg)=%ld != sum(row deg)=%ld\n",
                label, sum_col, sum_row);
    }

    // --- build ni2j (dynamic, 不用全域 M) ---
    int32_t *LM = (int32_t*)calloc(G->MM, sizeof(int32_t));
    for (n = 0; n < G->NN; n++) {
        G->ni2j[n] = calloc(G->num_n[n], sizeof(*(G->ni2j[n])));
        for (i = 0; i < G->num_n[n]; i++) {
            m = G->nlist[n][i];
            if ((unsigned)m >= (unsigned)G->MM) {
                fprintf(stderr, "nlist out-of-range: col=%d i=%d m=%d (MM=%d)\n", n,i,m,G->MM);
                exit(1);
            }
            G->ni2j[n][i] = LM[m];
            LM[m]++;
        }
    }
    free(LM);

    // --- 成功 ---
    printf("load %s done and success!\n", label);

    
    
}


