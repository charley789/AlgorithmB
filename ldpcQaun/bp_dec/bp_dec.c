static void HardDecision(QBP_Ctl *bp, a_matrix_GFQ *A)
{
  int32_t n;

  // bp->iter += 1;  // always do this first
  //  MOVED TO OUTSIDE (before calling this function)

#if POS_AVG == 0
  for (n = 0; n < A->NN; n++)
  {
    if (bp->qn[n][0] > bp->qn[n][1])
      if (bp->qn[n][2] > bp->qn[n][3])
        // if(bp->qn[n][2] >= bp->qn[n][3])       // prefer Z than Y
        if (bp->qn[n][0] > bp->qn[n][2])
          bp->tt[n] = 0;
        else
          bp->tt[n] = 2;
      else if (bp->qn[n][0] > bp->qn[n][3])
        bp->tt[n] = 0;
      else
        bp->tt[n] = 3;
    else // [1] >= [0]
      if (bp->qn[n][2] > bp->qn[n][3])
        if (bp->qn[n][1] > bp->qn[n][2])
          bp->tt[n] = 1;
        else
          bp->tt[n] = 2;
      else if (bp->qn[n][1] > bp->qn[n][3])
        bp->tt[n] = 1;
      else
        bp->tt[n] = 3;
    // if(bp->qn[n][1] >= bp->qn[n][3]) bp->tt[n] = 1;  else  bp->tt[n] = 3;   // prefer X than Y
  }

#else // POS_AVG==1
  int32_t base;
  double scal;

  //-- normalization
  for (n = 0; n < N; n++)
  {
    scal = 1.0 / (bp->qn[n][0] + bp->qn[n][1] + bp->qn[n][2] + bp->qn[n][3]);
    bp->qn[n][0] *= scal;
    bp->qn[n][1] *= scal;
    bp->qn[n][2] *= scal;
    bp->qn[n][3] *= scal;
  }

  //-- average
  if (bp->iter == 1)
    memcpy(bp->qn_avg, bp->qn, sizeof(bp->qn_avg));
  else
  {
    if (bp->iter < POS_AVG)
    {
      base = bp->iter;
    }
    else
    {
      base = POS_AVG;
    }
    for (n = 0; n < N; n++)
    {
      bp->qn_avg[n][0] = (bp->qn_avg[n][0] * (base - 1) + bp->qn[n][0]) / base;
      bp->qn_avg[n][1] = (bp->qn_avg[n][1] * (base - 1) + bp->qn[n][1]) / base;
      bp->qn_avg[n][2] = (bp->qn_avg[n][2] * (base - 1) + bp->qn[n][2]) / base;
      bp->qn_avg[n][3] = (bp->qn_avg[n][3] * (base - 1) + bp->qn[n][3]) / base;
    }
  }

  //-- hard decision (by qn_avg, not qn)
  for (n = 0; n < N; n++)
  {
    if (bp->qn_avg[n][0] > bp->qn_avg[n][1])
      if (bp->qn_avg[n][2] > bp->qn_avg[n][3])
        // if(bp->qn_avg[n][2] >= bp->qn_avg[n][3])       // prefer Z than Y
        if (bp->qn_avg[n][0] > bp->qn_avg[n][2])
          bp->tt[n] = 0;
        else
          bp->tt[n] = 2;
      else if (bp->qn_avg[n][0] > bp->qn_avg[n][3])
        bp->tt[n] = 0;
      else
        bp->tt[n] = 3;
    else // [1] >= [0]
      if (bp->qn_avg[n][2] > bp->qn_avg[n][3])
        if (bp->qn_avg[n][1] > bp->qn_avg[n][2])
          bp->tt[n] = 1;
        else
          bp->tt[n] = 2;
      else if (bp->qn_avg[n][1] > bp->qn_avg[n][3])
        bp->tt[n] = 1;
      else
        bp->tt[n] = 3;
    // if(bp->qn_avg[n][1] >= bp->qn_avg[n][3]) bp->tt[n] = 1;  else  bp->tt[n] = 3;   // prefer X than Y
  }
#endif
}

