//-- Matrix vector multiply A2*x = y
void Mat2VecMultiply(a2_matrix *A, uint8_t *x, uint8_t *y)
{
  int32_t n, n2m, m;
  memset(y, 0, A->MM * sizeof(*y));

  //-- handle by summing N (Mx1)_columns
  for (n = 0; n < A->NN; n++)
  {
    if (x[n])
    {
      for (n2m = 0; n2m < A->num_n[n]; n2m++)
      {
        m = A->nlist[n][n2m];
        y[m] ^= 1;
      }
    }
  }
}

//-- "Quantum" Gen Syndrome Ax = y (where A and x are over GF4 but y is over GF2 as additive code)
void Quan_GenSyndrome(a_matrix_GFQ *A, GFQ_t *x, GFQ_t *y) // input check matrix, and will check M = N-K rows
{
  int32_t n, n2m, m;
  memset(y, 0, A->MM * sizeof(*y));

  //-- handle by summing N (Mx1)_columns
  for (n = 0; n < A->NN; n++)
  {
    if (x[n])
    {
      for (n2m = 0; n2m < A->num_n[n]; n2m++)
      {
        m = A->nlist[n][n2m];
        y[m] ^= ((x[n] >> 1) & A->nlist_val[n][n2m]) ^ (x[n] & (A->nlist_val[n][n2m] >> 1));
        // printf("n %d  m %d  x[n] %d  A->nlist_val[n][n2m] %d y[m]%d\n", n,m,x[n],A->nlist_val[n][n2m],y[m]);
      }
    }
  }
  // printf("x = ");   for(n=0; n < A->NN ; n++) printf("%u ", x[n]);   printf("\n");
  // printf("y = ");   for(m=0; m < A->MM ; m++) printf("%u ", y[m]);   printf("\n");
}

void GenSyndrome_GF2(a2_matrix *A, GFQ_t *x, GFQ_t *y)
{
  memset(y, 0, A->MM * sizeof(*y));

  for (int n = 0; n < A->NN; n++)
  {
    if (!x[n])
      continue; /* 該 col 沒錯 → 跳過 */

    for (int k = 0; k < A->num_n[n]; k++)
    {
      int m = A->nlist[n][k]; /* col n 在 row m 有 1 */
      y[m] ^= 1;              /* GF(2)：累加就是 XOR */
    }
  }
}

